package waw.anotherworld.demon.services.corporation.model;

public class CorporationVO {

    private String corpName;
    private String corpCode;
    private String corpCategory;
    private String corpProduct;
    private String corpDay;
    private String corpHomepage;

    public String getCorpName() {
        return corpName;
    }

    public void setCorpName(String corpName) {
        this.corpName = corpName;
    }

    public String getCorpCode() {
        return corpCode;
    }

    public void setCorpCode(String corpCode) {
        this.corpCode = corpCode;
    }

    public String getCorpCategory() {
        return corpCategory;
    }

    public void setCorpCategory(String corpCategory) {
        this.corpCategory = corpCategory;
    }

    public String getCorpProduct() {
        return corpProduct;
    }

    public void setCorpProduct(String corpProduct) {
        this.corpProduct = corpProduct;
    }

    public String getCorpDay() {
        return corpDay;
    }

    public void setCorpDay(String corpDay) {
        this.corpDay = corpDay;
    }

    public String getCorpHomepage() {
        return corpHomepage;
    }

    public void setCorpHomepage(String corpHomepage) {
        this.corpHomepage = corpHomepage;
    }
}
